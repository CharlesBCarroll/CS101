/*This program finds the greatest common divsor between two integers*/
#include<iostream>
#include<cmath>
#include<cstdlib>
using namespace std;
int main()
{
    int a = 10, i = 1;

    for(int i = 0; i < 10; i++)
    {
        a += i;
        cout << i << ' ';
    }
    cout << endl;
    cout << a << " "<< i << endl;
}